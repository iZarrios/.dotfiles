--- Returns connected monitor names from `hyprctl monitors`.
--- @return string[]
local function get_monitors()
  local f = io.popen("hyprctl monitors")
  if f == nil then
    return {}
  end
  local output = f:read("*a") or ""
  f:close()
  local names = {}
  for name in string.gmatch(output, "Monitor ([^%s]+) %(") do
    names[#names + 1] = name
  end
  return names
end

local function get_hostname()
  local f = io.popen("/bin/hostnamectl hostname")
  if f == nil then
    return nil
  end
  local hostname = f:read("*a") or ""
  f:close()
  hostname = string.gsub(hostname, "\n$", "")
  return hostname
end

local hostname = get_hostname() or "biluka"

--- Applies a configuration function based on the current hostname.
--- @param f1 fun(): nil  -- Configuration function for "biluka"
--- @param f2 fun(): nil  -- Configuration function for "pavi"
local function apply_invariant(f1, f2)
  if hostname == "biluka" then
    f1()
  elseif hostname == "pavi" then
    f2()
  end
end


