  if w.fullscreen ~= 0 then
    hl.dispatch(hl.dsp.window.fullscreen_state({ internal = 0, client = 0, action = "set" }))
  else
    hl.dispatch(hl.dsp.window.fullscreen_state({ internal = 2, client = -1, action = "set" }))
  end
end)
